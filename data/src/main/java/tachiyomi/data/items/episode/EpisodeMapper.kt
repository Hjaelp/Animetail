package tachiyomi.data.items.episode

import tachiyomi.domain.items.episode.model.Episode

object EpisodeMapper {
    @Suppress("LongParameterList")
    fun mapEpisode(
        id: Long,
        animeId: Long,
        url: String,
        name: String,
        scanlator: String?,
        seen: Boolean,
        bookmark: Boolean,
        lastSecondSeen: Long,
        totalSeconds: Long,
        episodeNumber: Double,
        seriesNumber: Long?,
        airDate: Long?,
        title: String?,
        runtime: Long?,
        contentRating: String?,
        overview: String?,
        thumbnailUrl: String?,
        chapterBookmarks: String?,
        sourceOrder: Long,
        dateFetch: Long,
        dateUpload: Long,
        lastModifiedAt: Long,
        version: Long,
        @Suppress("UNUSED_PARAMETER")
        isSyncing: Long,
    ): Episode = Episode(
        id = id,
        animeId = animeId,
        seen = seen,
        bookmark = bookmark,
        lastSecondSeen = lastSecondSeen,
        totalSeconds = totalSeconds,
        dateFetch = dateFetch,
        sourceOrder = sourceOrder,
        url = url,
        name = name,
        dateUpload = dateUpload,
        episodeNumber = episodeNumber,
        scanlator = scanlator,
        description = overview,
        season = -1L,
        thumbnailUrl = thumbnailUrl,
        lastModifiedAt = lastModifiedAt,
        version = version,
        seriesNumber = seriesNumber,
        airDate = airDate,
        title = title,
        runtime = runtime,
        contentRating = contentRating,
        overview = overview,
        chapterBookmarks = chapterBookmarks,
    )
}
